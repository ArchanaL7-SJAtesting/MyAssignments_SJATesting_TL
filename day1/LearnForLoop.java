package week1.day1;

public class LearnForLoop {
	
	public static void main(String[] args) {
		
		//print numbers from 1 to 20
		
		//type for -->ctrl+space --> click 2nd for loop -- >click enter
		
		for(int i=1;i<=20;i++)
		{
			//i=1;1<=20->true ;1++
		
		System.out.println(i);//1 2 ..20
		
		}
		
		
	}
	
}
